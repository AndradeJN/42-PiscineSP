void	ft_rev_int_tab(int *tab, int size)
{
	int aux;
	int i;
	int j;

	j = size - 1;
	i = 0;
	while (i < size / 2)
	{
		aux = tab[i];
		tab[i] = tab[j];
		tab[j] = aux;
		i++;
		j--;
	}
}
